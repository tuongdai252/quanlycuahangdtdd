    DefaultTableModel modeltable = new DefaultTableModel(null, columns);
    
    void HienThiDS(ArrayList dshd) 
    {
        modeltable.getDataVector().removeAllElements();
        for (int i = 0; i < dshd.size(); i++) {
            chitiethoadonnhapDTO a = (chitiethoadonnhapDTO) dshd.get(i);
            modeltable.insertRow(i, new Object[]{a.masp, a.soluong, a.dongia, a.thanhtien});
        }
        jTable1.setModel(modeltable);
    }
    
    public XemChiTietHDN() {
        initComponents();
        ArrayList dshdn = chitiethoadonnhapBUS.TimKiemTatCa();
        HienThiDS(dshdn);
    }
    
