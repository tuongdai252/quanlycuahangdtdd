    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        String maquyen = (String) modeltable.getValueAt(jTable1.getSelectedRow(), 0);
        String quyen = (String) modeltable.getValueAt(jTable1.getSelectedRow(), 1);
        MaQuyenText.setText(maquyen);
        TenText.setText(quyen);
        MaQuyenText.enable();
        TenText.enable();
        SuaButton.setEnabled(true);
        XoaButton.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked


