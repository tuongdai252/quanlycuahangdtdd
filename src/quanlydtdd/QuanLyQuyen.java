    void HienThiDS(ArrayList ds) 
    {
        modeltable.getDataVector().removeAllElements();
        for (int i = 0; i < ds.size(); i++) {
            quyenDTO a = (quyenDTO) ds.get(i);
            modeltable.insertRow(i, new Object[]{a.maquyen, a.quyen});
        }
        jTable1.setModel(modeltable);
    }
    
